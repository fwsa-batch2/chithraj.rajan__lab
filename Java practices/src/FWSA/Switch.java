package FWSA;

public class Switch {
    public static void main(String[] args) {
        String hero = "ironman";
        switch (hero){

            case ("ironman"):{
                System.out.println("I am ironman");
                break;
            }
            case ("spiderman"):{
                System.out.println("Spidey here");
                break;
            }
            default:
                System.out.println("Marvel");
                break;


        }
    }
}
