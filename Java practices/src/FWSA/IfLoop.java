public class IfLoop {
    public static void main(String[] args) {

        int run = 50 ;

        if (run<50){
            System.out.println("Good Player");
        }
        else if (run == 50) {
            System.out.println("Half century");
        }
        else if (run > 50) {
            System.out.println("Century is near...Keep it up");
        }
        else {
            System.out.println("Fire is on");
        }

    }
}
